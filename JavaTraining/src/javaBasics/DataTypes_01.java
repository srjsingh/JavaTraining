package javaBasics;

public class DataTypes_01 {

	public static void main(String[] args) {
		
		// primitive data type int, char, double and boolean 
		
		// 1. integer
		int i = 10;
		
		// 2. double is used for decimal
		double d = 12.33; 
		
		// 3. char is for character
		char c = 'A'; 
		
		// 4. boolean is either true or false
		boolean b1 = true; 
		boolean b2 = false;
		
		// String is a class and not data type
		String s = "suraj";
		
		System.out.println(i + "," + d + "," + c + "," + b1 + "," + b2 + "," + s); 

	}

}
